def textbox(x, y, txt, col=7):  # col：*0～*F
    init_x = x
    init_col = col
    for ch in txt:
        if col == -1:
            if ch == '*':
                col = init_col
            else:
                col = int(ch, 16)
            continue
        elif ch == '*':
            col = -1
            continue
        if ch == '\n':
            x = init_x
            y += 12
        else:
            font_data = FONT_DIC.get(ch)
            if font_data == None:
                x += 8
            else:
                for dx, dy in zip(*[iter(font_data)]*2):
                    pyxel.pset(x+dx, y+dy, col)
                if ord(ch)<=0xff:
                    x += 4
                else:
                    x += 8
